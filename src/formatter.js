'use strict';

const { formatDateHuman, renderBar, formatNumber, padRight, padLeft } = require('./utils');

// ─────────────────────────────────────────────────────────
//  TERMINAL FORMATTER
// ─────────────────────────────────────────────────────────

/**
 * Format summaries as rich terminal output with Unicode box-drawing,
 * bar charts, and colour-coded activity heatmaps.
 *
 * @param {Array}  summaries - Array of repo summary objects
 * @param {Object} overall   - Merged overall summary
 * @param {Object} options   - { since: Date, until: Date, days: number }
 * @returns {string}
 */
function formatTerminal(summaries, overall, options = {}) {
  const { since, until, days } = options;
  const lines = [];
  const W = 62;            // box inner width
  const divider = '─'.repeat(W);

  // ── Header ──────────────────────────────────────────────
  lines.push('');
  lines.push('╔' + '═'.repeat(W) + '╗');
  lines.push('║' + centerPad('  WEEKLY GIT ACTIVITY SUMMARY', W) + '║');
  if (since && until) {
    const period = `${formatDateHuman(since)} – ${formatDateHuman(until)}`;
    lines.push('║' + centerPad(period, W) + '║');
  }
  lines.push('╚' + '═'.repeat(W) + '╝');
  lines.push('');

  // ── Overall block (multi-repo only) ─────────────────────
  if (summaries.length > 1) {
    lines.push(`  OVERALL  (${overall.repos} repositories)`);
    lines.push('  ' + divider);
    lines.push(`  Total commits    : ${formatNumber(overall.totalCommits)}`);
    lines.push(`  Files changed    : ${formatNumber(overall.totalFilesChanged)}`);
    lines.push(`  Insertions       : +${formatNumber(overall.totalInsertions)}`);
    lines.push(`  Deletions        : -${formatNumber(overall.totalDeletions)}`);
    lines.push(`  Active days      : ${overall.activeDays} / ${days || 7}`);
    lines.push('');
  }

  // ── Per-repo blocks ──────────────────────────────────────
  for (const summary of summaries) {
    lines.push(`  📁  ${summary.repoName}`);
    lines.push('  ' + divider);

    if (summary.totalCommits === 0) {
      lines.push('  No commits in this period.');
      lines.push('');
      continue;
    }

    lines.push(`  Commits          : ${formatNumber(summary.totalCommits)}`);
    lines.push(`  Authors          : ${summary.authors.length}`);
    lines.push(`  Files changed    : ${formatNumber(summary.filesChanged)}`);
    lines.push(`  Insertions       : +${formatNumber(summary.insertions)}`);
    lines.push(`  Deletions        : -${formatNumber(summary.deletions)}`);
    lines.push(`  Active days      : ${summary.activeDays} / ${days || 7}`);
    lines.push('');

    // Contributor bar chart
    if (summary.authors.length > 0) {
      lines.push('  Top contributors:');
      const maxC = summary.authors[0].commits;
      for (const author of summary.authors.slice(0, 6)) {
        const bar = renderBar(author.commits / maxC, 18);
        const pct = Math.round((author.commits / summary.totalCommits) * 100);
        const nameCol = padRight(author.name, 22);
        lines.push(`    ${nameCol} ${bar}  ${padLeft(author.commits, 4)} (${padLeft(pct, 3)}%)`);
      }
      lines.push('');
    }

    // Most active files
    if (summary.topFiles.length > 0) {
      lines.push('  Most active files:');
      lines.push(`    ${'File'.padEnd(42)} ${'+'+'Lines'.padStart(6)} ${'-'+'Lines'.padStart(6)}`);
      lines.push('    ' + '─'.repeat(58));
      for (const f of summary.topFiles.slice(0, 8)) {
        const fileCol = padRight(f.file, 42);
        lines.push(`    ${fileCol} ${padLeft('+' + f.added, 7)} ${padLeft('-' + f.deleted, 7)}`);
      }
      lines.push('');
    }

    // Daily activity heatmap
    const dayKeys = Object.keys(summary.commitsByDay).sort();
    if (dayKeys.length > 0) {
      lines.push('  Daily activity:');
      const maxDay = Math.max(...Object.values(summary.commitsByDay));
      for (const day of dayKeys) {
        const count = summary.commitsByDay[day];
        const bar = renderBar(count / maxDay, 22);
        lines.push(`    ${day}  ${bar}  ${padLeft(count, 3)}`);
      }
      lines.push('');
    }
  }

  lines.push('  ─── Generated by gitlog-weekly · npm install -g gitlog-weekly ───');
  lines.push('');

  return lines.join('\n');
}

