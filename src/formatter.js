/**
 * Format summaries as GitHub-flavored Markdown suitable for READMEs,
 * PR descriptions, CI artifacts, or stand-up reports.
 *
 * @param {Array}  summaries
 * @param {Object} overall
 * @param {Object} options
 * @returns {string}
 */
function formatMarkdown(summaries, overall, options = {}) {
  const { since, until, days } = options;
  const lines = [];

  lines.push('# Weekly Git Activity Summary');
  lines.push('');
  if (since && until) {
    lines.push(`**Period:** ${formatDateHuman(since)} – ${formatDateHuman(until)}`);
    lines.push('');
  }
  lines.push('> Generated by [gitlog-weekly](https://www.npmjs.com/package/gitlog-weekly)');
  lines.push('');

  // Overall section (multi-repo)
  if (summaries.length > 1) {
    lines.push('## Overall Statistics');
    lines.push('');
    lines.push('| Metric | Value |');
    lines.push('|--------|------:|');
    lines.push(`| Repositories | ${overall.repos} |`);
    lines.push(`| Total Commits | ${formatNumber(overall.totalCommits)} |`);
    lines.push(`| Files Changed | ${formatNumber(overall.totalFilesChanged)} |`);
    lines.push(`| Insertions | +${formatNumber(overall.totalInsertions)} |`);
    lines.push(`| Deletions | -${formatNumber(overall.totalDeletions)} |`);
    lines.push(`| Active Days | ${overall.activeDays} / ${days || 7} |`);
    lines.push('');

    if (overall.authors.length > 0) {
      lines.push('### All Contributors');
      lines.push('');
      lines.push('| Author | Commits | Share |');
      lines.push('|--------|--------:|------:|');
      for (const a of overall.authors) {
        const pct = overall.totalCommits > 0
          ? Math.round((a.commits / overall.totalCommits) * 100)
          : 0;
        lines.push(`| ${a.name} | ${a.commits} | ${pct}% |`);
      }
      lines.push('');
    }
  }

  // Per-repo sections
  for (const summary of summaries) {
    lines.push(`## 📁 ${summary.repoName}`);
    lines.push('');

    if (summary.totalCommits === 0) {
      lines.push('*No commits in this period.*');
      lines.push('');
      continue;
    }

    lines.push('| Metric | Value |');
    lines.push('|--------|------:|');
    lines.push(`| Commits | ${formatNumber(summary.totalCommits)} |`);
    lines.push(`| Authors | ${summary.authors.length} |`);
    lines.push(`| Files Changed | ${formatNumber(summary.filesChanged)} |`);
    lines.push(`| Insertions | +${formatNumber(summary.insertions)} |`);
    lines.push(`| Deletions | -${formatNumber(summary.deletions)} |`);
    lines.push(`| Active Days | ${summary.activeDays} / ${days || 7} |`);
    lines.push('');

    if (summary.authors.length > 0) {
      lines.push('### Contributors');
      lines.push('');
      lines.push('| Author | Commits | Share |');
      lines.push('|--------|--------:|------:|');
      for (const a of summary.authors) {
        const pct = Math.round((a.commits / summary.totalCommits) * 100);
        lines.push(`| ${a.name} | ${a.commits} | ${pct}% |`);
      }
      lines.push('');
    }

    if (summary.topFiles.length > 0) {
      lines.push('### Most Active Files');
      lines.push('');
      lines.push('| File | Changes | +Lines | -Lines |');
      lines.push('|------|--------:|-------:|-------:|');
      for (const f of summary.topFiles) {
        lines.push(`| \`${f.file}\` | ${f.changes} | +${f.added} | -${f.deleted} |`);
      }
      lines.push('');
    }

    const dayKeys = Object.keys(summary.commitsByDay).sort();
    if (dayKeys.length > 0) {
      lines.push('### Daily Activity');
      lines.push('');
      lines.push('| Date | Commits |');
      lines.push('|------|--------:|');
      for (const day of dayKeys) {
        lines.push(`| ${day} | ${summary.commitsByDay[day]} |`);
      }
      lines.push('');
    }
  }

  return lines.join('\n');
}

