/**
 * Center a string within a fixed character width, padding with spaces.
 * Handles multi-byte / emoji characters approximately.
 */
function centerPad(str, width) {
  // Approximate visual length (emoji count double)
  const visLen = [...str].reduce((acc, ch) => {
    const cp = ch.codePointAt(0);
    // Basic emoji range check for double-width characters
    return acc + (cp > 0x2000 ? 2 : 1);
  }, 0);

  const padding = Math.max(0, Math.floor((width - visLen) / 2));
  const result = ' '.repeat(padding) + str;
  const remaining = width - (padding + visLen);
  return result + ' '.repeat(Math.max(0, remaining));
}

module.exports = { formatTerminal, formatMarkdown, formatJSON };
