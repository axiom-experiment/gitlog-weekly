/**
 * Format summaries as machine-readable JSON — useful for CI pipelines,
 * dashboards, or downstream processing.
 *
 * @param {Array}  summaries
 * @param {Object} overall
 * @param {Object} options
 * @returns {string} Pretty-printed JSON
 */
function formatJSON(summaries, overall, options = {}) {
  return JSON.stringify(
    {
      generated_at: new Date().toISOString(),
      period: {
        since: options.since ? options.since.toISOString() : null,
        until: options.until ? options.until.toISOString() : null,
        days: options.days || 7
      },
      overall,
      repositories: summaries
    },
    null,
    2
  );
}

